#!/bin/bash

# Скрипт для запуска Django сервера на указанном IP и порту

echo "Запуск Django сервера на 192.168.3.16:8001..."
echo "Сервер будет доступен по адресу: http://192.168.3.16:8001"
echo ""

# Активируем виртуальную среду
source venv/bin/activate

# Запускаем сервер
python manage.py runserver 192.168.3.16:8001
